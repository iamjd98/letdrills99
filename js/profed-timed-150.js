  // Variables
  var countdownElement = document.getElementById("countdown");
  var startTime = Date.now(); // Store the start time of the quiz
  
  // Function to format time in hh:mm:ss format
  function formatTime(seconds) {
    var hours = Math.floor(seconds / 3600);
    var minutes = Math.floor((seconds % 3600) / 60);
    var remainingSeconds = seconds % 60;
  
    var timeString = hours.toString().padStart(2, "0") + ":" +
      minutes.toString().padStart(2, "0") + ":" +
      remainingSeconds.toString().padStart(2, "0");
  
    return timeString;
  }
  
  // Function to start the countdown timer
  function startTimer() {
    var elapsedTime = Math.floor((Date.now() - startTime) / 1000);
    var remainingSeconds = countdown - elapsedTime;
  
    countdownElement.textContent = formatTime(remainingSeconds);
  
    if (remainingSeconds <= 0) {
      clearInterval(countdownInterval);
      submitButton.click(); // Automatically submit the quiz when time runs out
    }
  
    countdownInterval = setInterval(function () {
      remainingSeconds--;
  
      countdownElement.textContent = formatTime(remainingSeconds);
  
      if (remainingSeconds <= 0) {
        clearInterval(countdownInterval);
        submitButton.click(); // Automatically submit the quiz when time runs out
      }
    }, 1000);
  }
  
  
